def make_upper(a_str):
        print(a_str.upper())


def make_lower(a_str):
        print(a_str.lower())


def make_capital(a_str):
        print(a_str.capitalize())




def main():
        # making a into upper 
        a = 'mahboob alam'

        # making b into lower 
        b = 'MAHBOOB ALAM'

        # making c into capitalized 
        c = 'mahboob alam'

        make_upper(a)
        make_lower(b)
        make_capital(c)


main()





